#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*d;
	int		i;

	d = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	i = 0;
	if (!d)
		return (0);
	while (s1[i])
	{
		d[i] = s1[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
